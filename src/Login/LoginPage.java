    public void login() {
        try {
            String query = "SELECT * FROM teacher WHERE Username='" + txtUserName.getText() + "' AND password='" + txtUserPassword.getText() + "' ;";
            ResultSet rs = db.getResultSet(query);
            if (rs.next()) {
                try {
                    this.dispose();
                    new StudentsEntry().setVisible(true);
                } catch (Exception ae) {
                    JOptionPane.showMessageDialog(null, ae);
                }
            } else {
                txtUserName.setText("");
                txtUserPassword.setText("");
                JOptionPane.showMessageDialog(this, "Wrong username or Password", "Error", JOptionPane.ERROR_MESSAGE);
                txtUserName.grabFocus();
            }
        } catch (SQLException | HeadlessException e) {
        }
    }

